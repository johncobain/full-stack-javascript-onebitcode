let x = parseFloat(prompt("Digite um número:"));
let y = parseFloat(prompt("Digite outro número:"));

let sum = x + y;
let sub = x - y;
let times = x * y;
let divide = x / y;

console.log(`Os números ${x} e ${y} possuem:
\nsoma ${sum}
\nsubtração ${sub}
\nmultiplicação ${times}
\ndivisão ${divide}`);